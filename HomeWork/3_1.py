# использования функции int

def des_to_dvoi(n):
    #десятичное число преобразуют к двоичному представлению путем нахождения остатков от деления на 2.
    # При этом полученное на предыдущем шаге частное выступает в качестве делимого на следующем шаге.
    # Деление заканчивается, когда делимое обращается в ноль. Остатки собираются в двоичное число начиная
    # с конца, то есть последний остаток будет первой цифрой двоичного числа.
    b = ''
    while n > 0:
        b = str(n % 2) + b
        n = n // 2
    return(b)

