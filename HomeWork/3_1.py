def dvoi_to_des(x):
    Kolichestvo_visel= len(x)
    chislo_dec = 0
    for i in range(0, Kolichestvo_visel):
        chislo_dec = chislo_dec + int(x[i]) * (2 ** (Kolichestvo_visel - i - 1))
    return chislo_dec

